package ObjectsandTools;

public class relativeName {
	
	public double score;
	public String name;
	public double rating;
	
	public relativeName(int score, String name) {
		this.score = score;
		this.name = name;
		this.rating = 0.0;
	}
	
	public relativeName(int score, String name, int rating) {
		this.score = score;
		this.name = name;
		this.rating = rating;
	}
	
}
